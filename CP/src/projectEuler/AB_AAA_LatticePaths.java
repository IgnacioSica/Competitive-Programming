package projectEuler;

public class AB_AAA_LatticePaths {
	
	public static void main (String [] args) {
		
		int grid [][] = new int [4][4];
		
		for (int x = 0; x < grid.length; x++) {
			for (int y = 0; y < grid[x].length; y++) {
				System.out.print(grid[x][y]);
			}
		}
		
//		40!/(20!*20!)
		
	}
	
}

